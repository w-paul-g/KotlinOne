// Loop Statements
//
fun main(args: Array<String>) {
    //For Loop

    //Displaying a list

    //e.g. List of Students
    var students = arrayOf("Joseph", "Irene", "John", "Andrew")
    for (listofstudents in students){
        println(listofstudents)
    }

    //While Loop

    //e.g. number
    var number = 5
    while (number<=10){
        println(number)
        number++
    }

}